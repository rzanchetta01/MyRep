package Ex_Feitos.problemasBasicos;
import java.util.Scanner;

//calcula conta cell se passar do valor limite

public class CalContaCell {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        double cobranca = 50.00;
        double minTel;
        minTel = sc.nextDouble();

        for (int x = 100; x < minTel; minTel = minTel - 1) {
            cobranca = cobranca + 2;
        }
        System.out.println("Valor a pagar:" + cobranca);

        sc.close();
    }
}
