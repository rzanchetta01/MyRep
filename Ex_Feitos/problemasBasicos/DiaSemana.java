package Ex_Feitos.problemasBasicos;
import java.util.Scanner;

//Ve qual dia da semana eh combase no numero

public class DiaSemana {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int dia;
        System.out.println("digite um numero de 1 a 7 para saber o correspondente na semana");
        dia = sc.nextInt();
        switch (dia) {
            case 1:
                System.out.println("é domingo");
                break;
            case 2:
                System.out.println("é segunda");
                break;
            case 3:
                System.out.println("é terça");
                break;
            case 4:
                System.out.println("é quarta");
                break;
            case 5:
                System.out.println("é quinta");
                break;
            case 6:
                System.out.println("é sexta");
                break;
            case 7:
                System.out.println("é sabado");
                break;

            default:
                System.out.println("dia invalido");
                break;
        }

        sc.close();
    }
}
